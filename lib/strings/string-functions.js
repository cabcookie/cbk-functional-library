export const upperCase = (val) => (typeof val === 'string') ? val.toUpperCase() : val;
export const capitalize = (str) => str.charAt(0).toUpperCase() + str.slice(1);
export const replace = (toReplace, replacyBy) => (str) => str.replace(toReplace, replacyBy);
export const str = (str) => () => str;
export const strFill = (fill, digits) => (str) => (`${fill}`.repeat(digits) + `${str}`).slice(-digits);
export const prepend = (pre) => (str) => `${pre}${str}`;
export const append = (post) => (str) => `${str}${post}`;
export const left = (count) => (str) => str.substring(0, count);
export const right = (count) => (str) => str.substring(str.length - count, str.length + 1);
export const mid = (first, length) => (str) => str.substring(first - 1, first + length - 1);
